// ============================================================
// 1. ИМПОРТЫ (внешние библиотеки)
// ============================================================

// express — фреймворк для создания сервера
// (даёт готовые инструменты: маршруты, middleware, обработку запросов)
import express from 'express';

// cors — библиотека для разрешения запросов с других портов
// (иначе браузер заблокирует запросы с фронта (порт 5173) к бекенду (порт 3000))
import cors from 'cors';

// ============================================================
// 2. СОЗДАЁМ ПРИЛОЖЕНИЕ EXPRESS
// ============================================================

// экземпляр приложения Express
// В переменной app теперь все маршруты и настройки
const app = express();

// порт, на котором будет слушать сервер
const PORT = 3000;

// ============================================================
// 3. MIDDLEWARE (промежуточные обработчики)
// ============================================================

// разрешаем запросы с любых источников
app.use(cors());

// парсер JSON — автоматически превращает входящий JSON в JavaScript объект
// Без этого в req.body при POST запросе было бы undefined
app.use(express.json());

// ============================================================
// 4. МАРШРУТЫ (ROUTES / ENDPOINTS)
// ============================================================

// тест запрос
app.get('/api/health', (req, res) => {
    // req — объект запроса (содержит данные от клиента: заголовки, параметры, тело)
    // res — объект ответа (через него отправляем данные обратно клиенту)
    
    // res.json() — отправляет ответ в формате JSON и автоматически ставит заголовок Content-Type: application/json
    res.json({ 
        status: 'ok',      // статус: ok — значит сервер жив
        message: 'API работает'   // сообщение для проверки
    });
});

// ============================================================
// 5. ЗАПУСК СЕРВЕРА
// ============================================================

// Функция, переданная вторым аргументом, выполнится после успешного запуска
app.listen(PORT, () => {
    console.log(`Сервер запущен на http://localhost:${PORT}`);
    console.log(`Проверка API: http://localhost:${PORT}/api/health`);
});
