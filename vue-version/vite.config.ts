// ============================================================
// 1. ИМПОРТЫ 
// ============================================================

// Импорт утилит Node.js для работы с путями к файлам
// fileURLToPath — превращает URL в путь к файлу (например, file:///C:/project/src → C:/project/src)
// URL — для создания URL-адресов
import { fileURLToPath, URL } from 'node:url';

// defineConfig — функция Vite для создания конфигурации
// даёт автодополнение и проверку типов (TypeScript)
import { defineConfig } from 'vite';

// плагин Vue — обязателен для работы с .vue файлами
// компилирует шаблоны, скрипты и стили из SFC (Single File Components)
import vue from '@vitejs/plugin-vue';

// плагин для Vue DevTools — удобные инструменты для отладки
// добавляет специальную панель в браузере для просмотра состояния компонентов
import vueDevTools from 'vite-plugin-vue-devtools';

// ============================================================
// 2. ЭКСПОРТ КОНФИГУРАЦИИ
// ============================================================

// export default — делает эту конфигурацию основной для проекта
// defineConfig — оборачивает объект и добавляет подсказки типов
export default defineConfig({
    // ============================================================
    // 3. ПЛАГИНЫ (расширяют возможности Vite)
    // ============================================================
    plugins: [
        vue(),           // Плагин Vue — ЯДРО, без него проект не заработает
        vueDevTools(),   // Плагин DevTools — опционально, помогает отлаживать
    ],

    // ============================================================
    // 4. НАСТРОЙКА СЕРВЕРА VITE
    // ============================================================
    server: {
        // Прокси — перенаправление запросов с фронта на бекенд
        // нужно, чтобы обойти CORS (ошибки с разными портами)
        proxy: {
            // Все запросы, начинающиеся с /api...
            '/api': {
                // ...перенаправлять на адрес бекенда (порт 3000)
                target: 'http://localhost:3000',
                // changeOrigin: true — подменяет origin запросф на target
                // чтобы бекенд не видел, что запрос пришёл с 5173 порта
                changeOrigin: true,
            }
        }
    },

    // ============================================================
    // 5. НАСТРОЙКА АЛИАСОВ (псевдонимов для путей)
    // ============================================================
    resolve: {
        alias: {
            // Символ @ в импортах заменяется на путь к папке src
            '@': fileURLToPath(new URL('./src', import.meta.url))
        }
    },
});
